def greaterThan(x, y):
    if x > y:
        return True
    else:
        return False 

print(greaterThan(10, 5))

def equalTo(x, y):
    if x == y:
        return True
    else:
        return False

print(equalTo(10, 5))

def lessOrEqual(x, y):
    if x <= y:
        return True
    else:
        return False 

print(lessOrEqual(10, 5))

